//Basics of Arrays in JavaScript

//Array Definition: Arrays are used to store multiple values in a single variable. In JavaScript, arrays are dynamic, meaning they can grow or shrink.

// Array Creation:
let arr = [1, 2, 3, 4, 5];

//Accessing Elements:
console.log(arr[0]); // Output: 1

// Common Operations:

// Push: Add an element to the end
arr.push(6); // [1, 2, 3, 4, 5, 6]

// Pop: Remove the last element
arr.pop(); // [1, 2, 3, 4, 5]

// Shift: Remove the first element
arr.shift(); // [2, 3, 4, 5]

// Unshift: Add an element to the beginning
arr.unshift(0); // [0, 2, 3, 4, 5]
